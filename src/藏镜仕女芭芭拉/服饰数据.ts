/**
 * 根据角色和服饰ID获取服装状态
 * @param role '芭芭拉' | '荧'
 * @param id 0~19，0~18对应共通服饰，19对应各自专属
 */
export function getOutfitByID(role: '芭芭拉' | '荧', id: number): 服装状态 & { 名称: string } {
  if (id < 0 || id > 19) throw new Error('服饰ID必须在0~19之间');
  if (id < COMMON_OUTFITS.length) {
    return { ...COMMON_OUTFITS[id] };
  }
  // id === 19 → 专属服饰
  return role === '芭芭拉'
    ? { ...芭芭拉专属服饰 }
    : { ...荧专属服饰 };
}
